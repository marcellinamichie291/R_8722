    В наших скитаниях, мы находили реку и Озеро, но остатся там мы не смогли. Вдоль берега там росли кусты с застрявшими в них скелетами. Черепа в этих кустах, были оплетены веткаим, осыпаны перьями и еловыми иголкаим, а запах исходящий от них воскрешал в памяти худшие епизоды нашего пути. Мы не решились ворошить гнезда и сбежали.

    Мы вырыли нору в глубокой торфяной залежи на Болоте. Внутри норы, вырыли помещения для сна, подвал и гостевую. Там можно было перемещатся согнувшись в трипогибели. Последним штрихом стал запасной выход, который вёл под воду и должен был затопить нору, если этим выходом воспользоватся. 
    
    - Сеее! Ааа... Ра!! - Сосед заманивал добычу песнями, подобными Зову. Эта вот мелодия была моей любимой, я так и не вспомнил его имя и дал ему новое. Саара. 
    
    Войдя в должности Исполнителей, мы начали "слышать" как Лес прорастает внутри нас. Я чуствовал как он касается меня внутри моей костяной клетки, это была черезвычайная интимность, испытать которую я никогда не хотел пока не попробовал. Стех пор Лесу больше не нужно было ждать пока мы съедим грибы, чтобы поговорить с нами.

    Мы менялись. Я все еще страдал от укусов насекомых, а насекомые, теперь, страдали когда кусали Саара. Мы выросли из наших трусов и потеряли их в конце концов. Песни Саара, становились сложнее, настолько сложнее что мне хотелось заглянуть в его глотку и узнать сколько у него голосовых связок.

    Мы научились играть, в Гляделку и Ловилку - чтобы различать мельчайшие изменения во мраке Леса и дополнять воображаемую карту местности - чтобы правильно интерпретировать световые пятна, которые Лес посылал нам на зрительный нерв, делясь с нами его собственной картой.

    Когда вонь со стороны Озера, достигла наших Болот, Саара сплел корзинку из камышей, лилий. Он сходил к Озеру и вернулся с десятком яиц.
    
    Скорлупы, целые и покоцанные. Куринные, перепелинные и Времяед весть, какие еще. Покоцанные, Саара заплетал в маленикие кошики которые он плел из водорослей. 

# АктБ. Плато. 4. адаптация 5. преобретение

