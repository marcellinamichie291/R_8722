Медитация 1. Лес.

***
    Мы проснулись от Зова. Лес разбудил нас. Шкребся в дверь спальни, пушистыми ветками. Звал Исполнителя.
    Мурашки за бегали у меня внутри, ритмично и настойчиво. Сумеречная дрема не отступала. Мои веки оставались прикрыты и не было силы способной распахнуть их до конца. Пришлось ориентироватся полагаясь на остальные чувства и запрокидывать лиццо вверх, чтобы лучше видеть дорогу впереди. Я не смог вспомнить имя соседа по комнате, в голове еще роились урывки невнятного сна.
    
    Сосед вышел в коридор, а я поплелся следом. Тусклый ночной свет в коридорах Дома, имел кобальтово синий оттенок.
    Дверь в уборную была стеклянная. Мы встретились взглядами в отражении дверного стекла. Колючие еловые лапы Леса пробежались от моего сердца, по левой руке, взяли моё запястье и ударилии им, соседа в левую лопатку.
    
    Мягкие ветвистые лапы обняли нас и из коридора утащили в Темнолесье. 
    
    Лес не пропускал солнца и ветер поднимался только из-за дождя. Под ногами слоились бурьяны и корни. Мы провели там достаточно, чтобы я начал скучать по рассветам и закатам. Из личных вещей, у нас были только трусы. Сосед пробовал разжечь костёр ветками и камняими. Не высек ни единной искры, но жопа сгорела.

    Когда начинался дождь, мы ловили воду ртом и собирали в ладони чтоб умытся и напится. От голода, спасались поедая кору и траву.

    Мы вышли из Темнолесья к деревьям покрытым светящимся мохом и светлячками. Мы ели пока Лес не начал погружался в непроглядный мрак. Тогда мы из трусов и мха сделали импровизированные светильники и пошли дальше.

    Нам встречались гигантские грибы-черношляпники и шыпастые цветы. Стебли последних, имели привычку резко распрямлятся и царапать ляжки когда мы подходили слишком близко. Здесь не встречались растения с листьями. Черношляпники были ядовиты, а их споры, которыми мы успели надышатся, были не менее вредны для нашего самочувствия.
    
    Все же иногда нам удавалось находить болеемение сьедобные штуки. Мы нашли грибы со шляпками выгнутыми краями вверх. Внутри чашечек они скапливали дождевую воду, светляков и мух. Рядом с этими грибами спали грызуны. Мы пили из них и они учили нас играть в Слушалку.

    Грызуны засыпающие у грибов стали частью нашего рациона. У нас появилось много свободного времени. Мы по долгу играли в Слушалку, Лес научил нас различать грызунов Пискунов от Таграбанчиков, различать грибы, сьедобные для тела и сьедобные только для разума. Лес читал нам сказки, о тех кого он кормит и защищает. Про Ходоков, Исполнителей, Скользящих, Скручивающих. Ему были нужны Исполнители для трансляции его воли.

    Я не давал согласия на назначение меня на какую либо из этих ролей. Но оказалось, что это не было ни прозьбой, ни предложением.

