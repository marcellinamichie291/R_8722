***
    
    Желтую Скорлупу, мы выбрали "слушать" первой. Мы уложили её на лист кувшинки. Ее нижнюю половину, оплели в мешочек из вродорослей и укрепили грязью. Верх был пробит. Я отрыгнул светлячка и запустил внутрь. Смог разглядеть скарлупки упавшие внутырь и блестящий янтарный желток. Белок отсутствовал.

    Мы лягли на спины, на пол в норе, ушами к Скарлупе. Выровняли дыхание. Смотрели на потолок норы, на котором я выращивал светящийся мох, он напоминал мне о ночном свете в коридорах Дома. Я нащупал нужное состояние и погрузился в Слушалку. Мерцание разлилось по глазам, от красных до синих цветов.

    Я отбрасывал сюжеты подсовываемые мне, моим сценарным апаратом. Я слушал Лес. Снаружи костяной клетки, внутри костяной клетки и его интерпретации того, что он видит внутри Скорлупы. У всего есть "душа". И о каждой "душе" Лес может тебе что-то рассказать, если ты умеешь "слушать". Я шептал в слух, картины показываемые мне Лесом, чтобы установить цикл обратной связи и установить связь с этой "душой".

    Картины захватили меня и на какое то мгновение, я забыл о мире за пределами Скорлупы.

    В небе светило Солнце. Саара находился в Озере, погруженный в воду по самые глаза. На берегу, сидел маленький старичек в рясе. Он сидел в кресле из желтого песка и ржавого мусора, который пускал потеки ржавчины от кресла в Озеро. Рядом с креслом, в песок был вбит ржавый меч.

    - Я ждал кого-то по больше. - Отозвался хазяин этого места, обращаясь к Саара.
    - Есть только я. - Они по молчали какое-то время. И Саара продолжил. - А кто был в прошлый раз?

    Лес отреагировал. Меня укутало в теплые и пушистые еловые лапки, закрыв обзор. Теплое дыхание заполнило уши и сразу отпустило. Я заметил не соответствие, между моими внутренним чувством времени и убежденностью что Лес сразу отпустил меня. Лес забрал у меня кусок памяти.

    - Так о чем мы? - Спросил Саара.
    - О том, о чем говорить нельзя. - Маленький старик встал. Вытащил свое оружие из песка и направил на Озеро. - Я не люблю когда меня чистят, словно я попался с простудой посреди Могильника. Следи за языком Исполнитель!

    Он улыбнулся и неуклюже провернул оружие, рисуя в воздухе круг. 

    Саара подплыл ближе и заговорил:
    - То что исполняло эту функцию ранее, не вышло на работу. Я не понимаю инструкции которые мне дает Лес. Мне сказали забрать всех кого смогу, прежде чем вас скормят некомпентным раздолбаям из Степей. Я сделал. Дальше мне нужно чтобы вы выполнили свои инстру...
    - Срань Господняя!!! - Вскричал собеседник, перебивая Саара. Он достал из своей рясы стеклянный пробник духов и метнул в Саара. Саара не отреагировал. Флакон упал в воду и пошел на дно. - Инструкция. Специально для такого душнилы как ты, Саара.
    - Мы знакомы? - Саара не спешил нырять за флаконом. Я "слушал" как флакон падает на дно в мутной воде.
    - Да. Я Amadan-na Breena. И то что, моим перемещением на новый Круг занимаешся ты... Я без понятия какого черта происходит... Так не бывает... Не может, бывать... Мой аватар, есть на каждом Круге, запомни моё лицо. Найди такое же и скорми ему содержимое Скорлупы. Тогда я, помогу тебе не быть съеденным теми самыми раздолбаями, которых ты взбесил угнав их добычу прямо у них из подноса. Да! Я знаю! Выживешь и я покажу тебе сон об этом, следущей ночью.

    Amadan-na Breena посмотрел мне в глаза и меня размазало по вневременному пространству. Лес оказался надомной, я слышал пульс с скрежет, словно от механичесного Сердца в центре которого я оказался. Я почувствовал себя коридором и каждое мое воспоминание, каждый мой секрет получил свою дверь в этом "коридоре". Breena ступил внутырь. Я разорвал зрительный контакт и открыл глаза трижды, выпрыгивая из "коридора", из "скорлупы" и из Слушалки.
    
    Я выполз из норы. 

    Саара уже был здесь. Играл в глядеки с девочкой лет шести.

    - Зачем тебе эти Скорлупы? - Спрашивала она и получала молчание в ответ. - Их бросили. Они уже начали портится, ты же слышишь запах. Мы хотим объеденить их, дать им новую жизнь.

